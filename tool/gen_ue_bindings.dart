// Generates the Unreal FSim* USTRUCT mirror of the render-frame tables in
// wire/sim.fbs, so the engine-facing structs stay in sync with the wire schema.
//
//   dart run tool/gen_ue_bindings.dart
//
// Types come from the schema; field NAMES come from the [structs] config below
// (the wire names are terse and the UE/BP names are nicer, and a few carry
// table-specific semantics — Building.pos is body-LOCAL, Vessel.pos is
// world-rebased — that the schema can't express). The generator VALIDATES drift:
// if a render table gains a field with no mapping here (or a mapping points at a
// field the schema doesn't have), it fails loudly — so you can't widen the wire
// and forget the UE side. The ingest/rebasing logic stays hand-written in
// SpaceSimSubsystem.cpp; only the data structs are generated.
import 'dart:io';

